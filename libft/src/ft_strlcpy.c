#include "libft.h"

size_t	ft_strlcpy(char *dst, char const *src, size_t size)
{
	int		src_len;
	size_t	i;

	src_len = ft_strlen(src);
	i = 0;
	if (size)
	{
		while (src[i] && i < size - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = 0;
	}
	return (src_len);
}
